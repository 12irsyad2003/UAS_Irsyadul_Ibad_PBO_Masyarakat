    private void text_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_text_cariActionPerformed
    try {
        // Query SQL untuk pencarian data
        String SQL = "SELECT * FROM pengajuan WHERE id_pengajuan LIKE '%" + text_cari.getText() + "%' OR layanan LIKE '%" + text_cari.getText() + "%'";

        // Judul kolom untuk tabel
        String judulkolom[] = {"ID PENGAJUAN", "LAYANAN", "STATUS PENGAJUAN", "KODE TRACKING"};

        // Lebar kolom disesuaikan (disesuaikan dengan jumlah kolom yang ada)
        int lebarKolom[] = {100, 300, 150, 200}; // Menambahkan lebar untuk kolom 'KODE TRACKING'

        // Menampilkan data ke tabel
        mycrud.tampilTabel(tbl_pengajuan, SQL, judulkolom);
        
        // Mengatur lebar kolom tabel
        mycrud.settingLebarKolom(tbl_pengajuan, lebarKolom);
    } catch (Exception e) {
        // Menangkap dan mencetak kesalahan
        System.err.println("Error: " + e.toString());
    }
    }//GEN-LAST:event_text_cariActionPerformed

