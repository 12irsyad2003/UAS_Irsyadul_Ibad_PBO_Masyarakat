    private void btn_ubah_pengajuanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ubah_pengajuanActionPerformed
                                              
    try {
        if (
            (text_idPengajuan.getText().equals("")) &&
            (text_Layanan.getText().equals("")) &&
            (text_status.getText().equals("")) &&
            (text_kodeTracking.getText().equals(""))
        ) {
            JOptionPane.showMessageDialog(this, "Silahkan Pilih Data yang Ingin di Ubah");
            tbl_pengajuan.requestFocus();
        } else {
            String fieldnya[] = {"layanan", "status_pengajuan", "kode_tracking"};
            String isiFieldnya[] = {
                text_Layanan.getText(),
                text_status.getText(),
                text_kodeTracking.getText()
            };
            obj.UbahDinamis("pengajuan", "id_pengajuan", text_idPengajuan.getText(), fieldnya, isiFieldnya);
            JOptionPane.showMessageDialog(this, "Data Berhasil Diubah");

            // Refresh tabel dan atur lebar kolom
            mycrud.tampilTabel(tbl_pengajuan, Sql, judulkolom);
            mycrud.settingLebarKolom(tbl_pengajuan, lebarkolom);
        }
    } catch (Exception e) {
        System.err.println(e.toString());
    }
    }//GEN-LAST:event_btn_ubah_pengajuanActionPerformed

    private void tbl_pengajuanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_pengajuanMouseClicked
    try {
        // Ambil baris yang dipilih
        int baris = tbl_pengajuan.getSelectedRow();
        text_idPengajuan.setText(tbl_pengajuan.getValueAt(baris, 0).toString());
        text_Layanan.setText(tbl_pengajuan.getValueAt(baris, 1).toString());
        text_status.setText(tbl_pengajuan.getValueAt(baris, 2).toString());
        text_kodeTracking.setText(tbl_pengajuan.getValueAt(baris, 3).toString());
    } catch (Exception e) {
        System.err.println(e.toString());
    }
    }//GEN-LAST:event_tbl_pengajuanMouseClicked

    private void btn_hapus_pengajuanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapus_pengajuanActionPerformed
    try {
        if (
                (text_idPengajuan.getText().equals("")) && 
                (text_Layanan.getText().equals("")) && 
                (text_status.getText().equals("")) && 
                (text_kodeTracking.getText().equals(""))
            )
        {
            JOptionPane.showMessageDialog(this, "Silahkan Pilih data yang Ingin dihapus");
            tbl_pengajuan.requestFocus();
        } else {
            String id_pengajuan = text_idPengajuan.getText();
            obj.HapusDinamis("pengajuan", "id_pengajuan", id_pengajuan);
            mycrud.tampilTabel(tbl_pengajuan, Sql, judulkolom);
            mycrud.settingLebarKolom(tbl_pengajuan, lebarkolom);
        }
    } catch (Exception e) {
        System.err.println(e.toString());
    }
    }//GEN-LAST:event_btn_hapus_pengajuanActionPerformed

